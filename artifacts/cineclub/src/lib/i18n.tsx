export type TranslationKeys = keyof typeof translations.tr;

const LangContext = createContext<{
  lang: Lang;
  setLang: (l: Lang) => void;
  t: (key: TranslationKeys) => string;
}>({
  lang: "tr",
  setLang: () => {},
  t: (key) => translations.tr[key] || key,
});

export function LangProvider({ children }: { children: ReactNode }) {
  const [lang, setLang] = useState<Lang>(() => {
    try { return (localStorage.getItem("cineclubLang") as Lang) || "tr"; } catch { return "tr"; }
  });

  const setLangAndSave = (l: Lang) => {
    setLang(l);
    try { localStorage.setItem("cineclubLang", l); } catch {}
  };

  const t = (key: TranslationKeys): string => {
    const dict = translations[lang] as Record<string, string>;
    const fallback = translations.tr as Record<string, string>;
    return dict[key] || fallback[key] || key;
  };

  return (
    <LangContext.Provider value={{ lang, setLang: setLangAndSave, t }}>
      {children}
    </LangContext.Provider>
  );
}

export function useLang() {
  return useContext(LangContext);
}

